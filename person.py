class Person():
    def __init__(self, name, age, status, hobby, field):
        self.name = name
        self.age = age
        self.status = status
        self.hobby = hobby
        self.field = field

    def toString():
        return self.name + ", Age: " + self.age + ", Hobbies: " + self.hobby + ", Field of Experience/Interest: " + self.field
